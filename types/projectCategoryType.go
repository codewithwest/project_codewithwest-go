package types

import "github.com/graphql-go/graphql"

var ProjectCategoryType = graphql.NewObject(
	graphql.ObjectConfig{
		Name: "ProjectCategory",
		Fields: graphql.Fields{
			"id": &graphql.Field{
				Type: graphql.Int,
			},
			"name": &graphql.Field{
				Type: graphql.String,
			},
			"created_at": &graphql.Field{
				Type: graphql.String,
			},
			"updated_at": &graphql.Field{
				Type: graphql.String,
			},
		},
	},
)
